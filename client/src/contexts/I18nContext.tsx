interface I18nValue {
  lang: Language;
  setLang: (lang: Language) => void;
  t: (key: string) => string;
}

const I18nContext = createContext<I18nValue | null>(null);

function resolvePath(dict: Dictionary, path: string): string | undefined {
  const parts = path.split('.');
  let current: string | Dictionary | undefined = dict;

  for (const part of parts) {
    if (!current || typeof current === 'string') return undefined;
    current = current[part];
  }

  return typeof current === 'string' ? current : undefined;
}

export function I18nProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<Language>(() => {
    if (typeof window === 'undefined') return 'ru';
    const raw = window.localStorage.getItem('prof-ai-lang');
    return raw === 'kk' ? 'kk' : 'ru';
  });

  const setLang = (nextLang: Language) => {
    setLangState(nextLang);
    if (typeof window !== 'undefined') {
      window.localStorage.setItem('prof-ai-lang', nextLang);
    }
  };

  const t = (key: string): string => {
    const direct = resolvePath(dictionary[lang], key);
    if (direct) return direct;

    const fallback = resolvePath(dictionary.ru, key);
    if (fallback) return fallback;

    return key;
  };

  const value = useMemo<I18nValue>(() => ({ lang, setLang, t }), [lang]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const context = useContext(I18nContext);
  if (!context) {
    throw new Error('useI18n must be used inside I18nProvider');
  }
  return context;
}
